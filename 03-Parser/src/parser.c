void help() { puts("\n$ ./parser <file>\n"); }

void error(char *msg) { printf("\nerror: %s \n", msg); }

int main(int argc, char **argv)
{
	if(argc != 2) { help(); return EXIT_SUCCESS; }

	FILE *file;

	if (!(file = fopen(argv[1], "r"))) {
		error("can't open file");
		return EXIT_SUCCESS;
	}

	Token token;

	do { token = scan_next(file); scanner_print(token); } while (token != EOT);

	rewind(file);

	puts("\n");

	parse(file);

    return EXIT_SUCCESS;
}
